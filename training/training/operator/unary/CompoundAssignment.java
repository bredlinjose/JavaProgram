package training.operator.unary;

public class CompoundAssignment {

	public static void main(String[] args) {
		int a=120;
		System.out.println(a+=50);
		System.out.println("Value of a is "+a);
		System.out.println(a-=20);
		System.out.println("Value of a is "+a);
		System.out.println(a*=2);
		System.out.println("Value of a is "+a);
		System.out.println(a%=2);
		System.out.println("Value of a is "+a);
		System.out.println(a/=2);
		System.out.println("Value of a is "+a);


		
	}

}
